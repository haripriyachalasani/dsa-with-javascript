// Goal: Learn basic arthimetics using variables 

let num1 = 10;
let num2 = 20;

let sum = num1 + num2;
console.log("The sum of", num1, "and", num2, "is:", sum);

let difference = num2 - num1;
console.log("The difference when", num1, "is subtracted from", num2, "is:", difference);

let product = num1 * num2;
console.log("The product of", num1, "and", num2, "is:", product);

let quotient = num2 / num1;
console.log("The quotient when", num2, "is divided by", num1, "is:", quotient);

let remainder = num2 % num1;
console.log("The remainder when", num2, "is divided by", num1, "is:", remainder);

let incrementedNum1 = ++num1;
console.log("The value of num1 after incrementing is:", incrementedNum1);

let decrementedNum2 = --num2;
console.log("The value of num2 after decrementing is:", decrementedNum2);

